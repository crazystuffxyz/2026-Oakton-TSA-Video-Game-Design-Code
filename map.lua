function map.draw()
    for i1 = 1, #map do 
        for i2 = 1, #map[i1] do
            local spriteToDraw = map.mapsprites[map[i1][i2]]
            if spriteToDraw then
                posx = (i2 - 1) * map.metadata.tileDimensions
                posy = (i1 - 1) * map.metadata.tileDimensions
                love.graphics.draw(spriteToDraw,posx,posy)
            end
        end
    end
end


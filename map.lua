function map.draw()
    for i1 = 1, #map.data do
        for i2 = 1, #map.data[i1] do
            local spriteToDraw = map.mapsprites[map.data[i1][i2]]
            if spriteToDraw then
                local posx = (i2 - 1) * map.metadata.tileDimensions
                local posy = (i1 - 1) * map.metadata.tileDimensions
                love.graphics.draw(spriteToDraw, posx, posy)
            end
        end
    end
end

